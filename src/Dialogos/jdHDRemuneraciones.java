    private void btCtaContableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCtaContableActionPerformed
        
        String Query="";

        if(cbHD.getSelectedItem().toString().trim().equals("HABERES")){
            
            Query = "SELECT codigo,nombre \n" +
                    "FROM conta_plan \n" +
                    "WHERE codigo like '2%' \n" +
                    "AND nivel IN (4) \n" +
                    "ORDER BY nombre";
            
        }else if(cbHD.getSelectedItem().toString().trim().equals("DESCUENTOS")){
            
            Query = "SELECT codigo,nombre \n" +
                    "FROM conta_plan \n" +
                    "WHERE codigo like '2%' \n" +
                    "AND nivel IN (4) \n" +
                    "ORDER BY nombre";
        
        }else if(cbHD.getSelectedItem().toString().trim().equals("OTROS")){
            
            Query = "SELECT codigo,nombre \n" +
                    "FROM conta_plan \n" +
                    "WHERE codigo like '2%' \n" +
                    "AND nivel IN (4) \n" +
                    "ORDER BY nombre";
        
        }
     
        jdAgregaDato Dato = new jdAgregaDato(null, true);
        Dato.setLocationRelativeTo(null);
        Dato.setTitle("Cuenta Contable");
        Dato.CargaDatos(Query);
        Dato.setVisible(true);

        if(!Dato.GetCodigo().isEmpty()){
            
            ExeSql Sql = new ExeSql();
            String Tipo = "";
           
               
            if(cbHD.getSelectedItem().toString().trim().equals("HABERES")){
                
                Tipo = "REM_HABERES";
        
            }else if(cbHD.getSelectedItem().toString().trim().equals("DESCUENTOS")){
            
                Tipo = "REM_DESCUENTOS";
        
            }else if(cbHD.getSelectedItem().toString().trim().equals("OTROS")){
        
                Tipo = "REM_OTROS";
            }
            
            
            try{
                Sql.ExeSql("UPDATE par_general\n" +
                           "SET cuenta='"+ Dato.GetCodigo().trim() +"'\n" +
                           "WHERE Codigo=" + GrillaHD.getValueAt(GrillaHD.getSelectedRow(), 0).toString().trim() + "\n"+
                           "AND tipo='" + Tipo + "'");
                
                Sql.Commit();
                txCtaContableCod.setText(Dato.GetCodigo().trim());
                txCtaContable.setText(Dato.GetNombre().trim());
            }catch (Exception e) {
                System.out.println(e.getMessage());
            }finally{
                Sql.Close();
            }

        }
    }//GEN-LAST:event_btCtaContableActionPerformed
    private void CargaDetalleHD(){
        
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        String Tipo="";
        
        btCtaContable.setEnabled(true);
           
        if(cbHD.getSelectedItem().toString().trim().equals("HABERES")){
        
            Tipo = "REM_HABERES";
        
        }else if(cbHD.getSelectedItem().toString().trim().equals("DESCUENTOS")){
            
            Tipo = "REM_DESCUENTOS";
        
        }else if(cbHD.getSelectedItem().toString().trim().equals("OTROS")){
        
            Tipo = "REM_OTROS";
        }
        
        try{
            Rs = Sql.Select("SELECT p.vigente,p.valor,p.cuenta,c.nombre\n" +
                            "FROM par_general p\n" +
                            "LEFT JOIN conta_plan c ON p.cuenta=c.codigo " +
                            "WHERE p.tipo='"+ Tipo +"'\n" +
                            "AND p.codigo=" + GrillaHD.getValueAt(GrillaHD.getSelectedRow(),0).toString().trim());
            
            if(Sql.GetRowCount()>0){
                
               Rs.next();
               chkVigente.setSelected(Rs.getBoolean("vigente"));
               chkPago.setSelected(Rs.getBoolean("valor"));
               txCtaContableCod.setText(Rs.getString("cuenta").trim());
               txCtaContable.setText(Rs.getString("nombre").trim());
                
            }
        }catch (Exception e) {
            
            System.out.println(e.getMessage());
            txCtaContable.setText("");
            txCtaContableCod.setText("");
        
        }finally{
            Sql.Close();
        }
    }
    
    private void CargaHD(){
        
        String Query = "";
        
        if(cbHD.getSelectedIndex() == -1) return;
        
        if(cbHD.getSelectedItem().toString().trim().equals("HABERES")){
            
            Query = "SELECT codigo,nombre,vigente\n" +
                    "FROM par_general\n" +
                    "WHERE tipo='REM_HABERES'\n" +
                    "ORDER BY nombre";
            
        }else if(cbHD.getSelectedItem().toString().trim().equals("DESCUENTOS")){
            
            Query = "SELECT codigo,nombre,vigente\n" +
                    "FROM par_general\n" +
                    "WHERE tipo='REM_DESCUENTOS'\n" +
                    "ORDER BY nombre";  
            
        }else if(cbHD.getSelectedItem().toString().trim().equals("OTROS")){
            
            Query = "SELECT codigo,nombre,vigente\n" +
                    "FROM par_general\n" +
                    "WHERE tipo='REM_OTROS'\n" +
                    "ORDER BY nombre";    
        }else if(cbHD.getSelectedItem().toString().trim().equals("TOTALES")){
            
            Query = "SELECT codigo,nombre,vigente\n" +
                    "FROM par_general\n" +
                    "WHERE tipo='REM_TOTALES'\n" +
                    "ORDER BY nombre";    
        }
        
        if(cbHD.getSelectedIndex() > -1){
            
            ExeSql Sql = new ExeSql();
            ResultSet Rs;
            DefaultTableModel Model = (DefaultTableModel)GrillaHD.getModel();
            
            try {
                Rs = Sql.Select(Query);
                fmMain.LimpiaGrilla(Model);
                
                while(Rs.next()){
                    
                    Model.addRow(new Object[]{Rs.getInt("codigo"), Rs.getString("nombre"), Rs.getBoolean("vigente")});
                           
                }
            } catch (Exception e) {
            }finally{
                Sql.Close();
            }
        }
            
            
        
    }
    private void btNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNuevoActionPerformed
        
        if(fmMain.OkCancel("¿Desea crear un nuevo " + cbHD.getSelectedItem().toString().trim())== JOptionPane.CANCEL_OPTION)
            return;
        
        String NvoHD = JOptionPane.showInputDialog("Nuevo " + cbHD.getSelectedItem().toString().trim());
        NvoHD = NvoHD.toUpperCase();
        String Tipo = "";
        
        if(cbHD.getSelectedItem().toString().trim().equals("HABERES")){
        
            Tipo = "REM_HABERES";
        
        }else if(cbHD.getSelectedItem().toString().trim().equals("DESCUENTOS")){
            
            Tipo = "REM_DESCUENTOS";
        
        }else if(cbHD.getSelectedItem().toString().trim().equals("OTROS")){
        
            Tipo = "REM_OTROS";
            
        }else if(cbHD.getSelectedItem().toString().trim().equals("TOTALES")){
        
            Tipo = "REM_TOTALES";
        }
        
        
        if(NvoHD!=null || !NvoHD.isEmpty()){
            
            ExeSql Sql = new ExeSql();
            try {
                
                Sql.ExeSql("INSERT INTO par_general\n" +
                           "(tipo,nombre) VALUES \n" +
                           "('"+ Tipo +"','"+ NvoHD +"')");
                Sql.Commit();
                CargaHD();
            
            }catch (Exception e) {
            
                fmMain.Mensaje(e.getMessage());
            
            }finally{
               
                Sql.Close();
            }
        }
    }//GEN-LAST:event_btNuevoActionPerformed

