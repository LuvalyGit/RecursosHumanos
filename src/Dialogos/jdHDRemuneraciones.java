    private void chkVigenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkVigenteActionPerformed
        
        if(GrillaHD.getSelectedRow()>=0){
            
          int Estado;
          String Tipo = "";
            
          if(chkVigente.isSelected())
             Estado=1;
          else
             Estado=0;
            
          if(cbHD.getSelectedItem().toString().trim().equals("HABERES")){
        
             Tipo = "REM_HABERES";
        
          }else if(cbHD.getSelectedItem().toString().trim().equals("DESCUENTOS")){
            
                Tipo = "REM_DESCUENTOS";
        
          }else if(cbHD.getSelectedItem().toString().trim().equals("OTROS")){
        
               Tipo = "REM_OTROS";
          }
            
          ExeSql Sql = new ExeSql();
            
          try{
              Sql.ExeSql("UPDATE par_general\n" +
                         "SET vigente="+ Estado +"\n" +
                         "WHERE tipo='"+ Tipo +"'\n" +
                         "AND codigo=" + GrillaHD.getValueAt(GrillaHD.getSelectedRow(),0).toString());
                
              GrillaHD.setValueAt(chkVigente.isSelected(), GrillaHD.getSelectedRow(), 2);
              Sql.Commit();
                
          } catch (Exception e) {
                fmMain.Mensaje(e.getMessage());
          } finally{
                Sql.Close();
          }
        }
    }//GEN-LAST:event_chkVigenteActionPerformed

